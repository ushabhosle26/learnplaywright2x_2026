// For Loop
// Help you to repeat a block of code.
console.log("******** Loop started from 1 to 10 **********");
for(let i=1; i<=10;i++)
{
    console.log(i);

}
console.log("******** Loop started from 10 to 1 **********");
for(let i=10; i>=1;i--)
{
    console.log(i);

}